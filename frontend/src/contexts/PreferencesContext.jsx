export function PreferencesProvider({ children }) {
  const [preferences, setPreferences] = useState(() => {
    // Try to load from localStorage first for faster initial render
    const saved = localStorage.getItem('userPreferences')
    if (saved) {
      try {
        return { ...DEFAULT_PREFERENCES, ...JSON.parse(saved) }
      } catch (e) {
        return DEFAULT_PREFERENCES
      }
    }
    return DEFAULT_PREFERENCES
  })
  const [loading, setLoading] = useState(true)

  // Load preferences from API on mount
  useEffect(() => {
    const loadPreferences = async () => {
      const token = localStorage.getItem('token')
      if (!token) {
        setLoading(false)
        return
      }

      try {
        const res = await api.get('/user/preferences')
        if (res.data?.data) {
          const prefs = { ...DEFAULT_PREFERENCES, ...res.data.data }
          setPreferences(prefs)
          localStorage.setItem('userPreferences', JSON.stringify(prefs))
        }
      } catch (error) {
        console.error('Failed to load preferences:', error)
      } finally {
        setLoading(false)
      }
    }

    loadPreferences()
  }, [])

  // Update preferences
  const updatePreferences = useCallback(async (newPrefs) => {
    const updated = { ...preferences, ...newPrefs }
    setPreferences(updated)
    localStorage.setItem('userPreferences', JSON.stringify(updated))

    try {
      await api.put('/user/preferences', updated)
    } catch (error) {
      console.error('Failed to save preferences:', error)
    }
  }, [preferences])

  // Format date according to user preference
  const formatDate = useCallback((timestamp) => {
    if (!timestamp) return '-'
    
    try {
      const date = new Date(timestamp)
      if (isNaN(date.getTime())) return '-'

      const day = String(date.getDate()).padStart(2, '0')
      const month = String(date.getMonth() + 1).padStart(2, '0')
      const year = date.getFullYear()

      switch (preferences.dateFormat) {
        case 'MM/DD/YYYY':
          return `${month}/${day}/${year}`
        case 'YYYY-MM-DD':
          return `${year}-${month}-${day}`
        case 'DD/MM/YYYY':
        default:
          return `${day}/${month}/${year}`
      }
    } catch (error) {
      return '-'
    }
  }, [preferences.dateFormat])

  // Format time according to user preference
  const formatTime = useCallback((timestamp) => {
    if (!timestamp) return '-'
    
    try {
      const date = new Date(timestamp)
      if (isNaN(date.getTime())) return '-'

      const hours = date.getHours()
      const minutes = String(date.getMinutes()).padStart(2, '0')
      const seconds = String(date.getSeconds()).padStart(2, '0')

      if (preferences.timeFormat === '12h') {
        const period = hours >= 12 ? 'PM' : 'AM'
        const hours12 = hours % 12 || 12
        return `${hours12}:${minutes}:${seconds} ${period}`
      }

      return `${String(hours).padStart(2, '0')}:${minutes}:${seconds}`
    } catch (error) {
      return '-'
    }
  }, [preferences.timeFormat])

  // Format date and time together
  const formatDateTime = useCallback((timestamp) => {
    if (!timestamp) return '-'
    const dateStr = formatDate(timestamp)
    const timeStr = formatTime(timestamp)
    if (dateStr === '-' || timeStr === '-') return '-'
    return `${dateStr}, ${timeStr}`
  }, [formatDate, formatTime])

  // Format relative time (e.g., "2 hours ago")
  const formatRelativeTime = useCallback((timestamp) => {
    if (!timestamp) return '-'
    
    try {
      const date = new Date(timestamp)
      if (isNaN(date.getTime())) return '-'

      const now = new Date()
      const diffMs = now - date
      const diffSec = Math.floor(diffMs / 1000)
      const diffMin = Math.floor(diffSec / 60)
      const diffHour = Math.floor(diffMin / 60)
      const diffDay = Math.floor(diffHour / 24)

      if (diffSec < 60) return 'Just now'
      if (diffMin < 60) return `${diffMin} minute${diffMin > 1 ? 's' : ''} ago`
      if (diffHour < 24) return `${diffHour} hour${diffHour > 1 ? 's' : ''} ago`
      if (diffDay < 7) return `${diffDay} day${diffDay > 1 ? 's' : ''} ago`

      return formatDate(timestamp)
    } catch (error) {
      return '-'
    }
  }, [formatDate])

  // Simple translation helper
  const t = useCallback((key) => {
    const lang = preferences.language || 'en'
    return translations[lang]?.[key] || translations['en']?.[key] || key
  }, [preferences.language])

  const value = {
    preferences,
    loading,
    updatePreferences,
    formatDate,
    formatTime,
    formatDateTime,
    formatRelativeTime,
    t,
    // Convenience getters
    itemsPerPage: preferences.itemsPerPage,
    defaultView: preferences.defaultView,
    dateFormat: preferences.dateFormat,
    timeFormat: preferences.timeFormat,
    language: preferences.language,
    timezone: preferences.timezone
  }

  return (
    <PreferencesContext.Provider value={value}>
      {children}
    </PreferencesContext.Provider>
  )
}

// Custom hook to use preferences
export function usePreferences() {
  const context = useContext(PreferencesContext)
  if (!context) {
    // Return default values if used outside provider (for backwards compatibility)
    return {
      preferences: DEFAULT_PREFERENCES,
      loading: false,
      updatePreferences: () => {},
      formatDate: (ts) => {
        if (!ts) return '-'
        const d = new Date(ts)
        return `${String(d.getDate()).padStart(2,'0')}/${String(d.getMonth()+1).padStart(2,'0')}/${d.getFullYear()}`
      },
      formatTime: (ts) => {
        if (!ts) return '-'
        const d = new Date(ts)
        return `${String(d.getHours()).padStart(2,'0')}:${String(d.getMinutes()).padStart(2,'0')}:${String(d.getSeconds()).padStart(2,'0')}`
      },
      formatDateTime: (ts) => {
        if (!ts) return '-'
        const d = new Date(ts)
        return `${String(d.getDate()).padStart(2,'0')}/${String(d.getMonth()+1).padStart(2,'0')}/${d.getFullYear()}, ${String(d.getHours()).padStart(2,'0')}:${String(d.getMinutes()).padStart(2,'0')}:${String(d.getSeconds()).padStart(2,'0')}`
      },
      formatRelativeTime: (ts) => ts ? 'Just now' : '-',
      t: (key) => key,
      itemsPerPage: 15,
      defaultView: 'list',
      dateFormat: 'DD/MM/YYYY',
      timeFormat: '24h',
      language: 'en',
      timezone: 'Asia/Kuala_Lumpur'
    }
  }
  return context
}

